import pandas as pd
import numpy as np

# Create dataset
data = {
    "marks": [24, 56, 79, 78, 66, 45],
    "age": [12, 34, 23, 16, 17, 19]
}

# Convert to DataFrame
df = pd.DataFrame(data)

print(df)
print(df.head())
print(df.info())
print(df.describe())

# Features and label
X = df[['age', 'marks']]

# Handle missing values
df.fillna(0, inplace=True)

# Filter passed students
student_passed = df[df['marks'] >= 50]
print(student_passed)

# Add status column
df['status'] = df['marks'].apply(
    lambda x: "Pass" if x >= 50 else "Fail"
)

# Save cleaned data
df.to_csv("cleaned_data.csv", index=False)

